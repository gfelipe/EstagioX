package com.ideais.interview;

public class Grades {

	private int grade1;
	private int grade2;
	private int grade3;
	
	public Grades(int grade1, int grade2, int grade3) {
		this.grade1 = grade1;
		this.grade2 = grade2;
		this.grade3 = grade3;
	}

	public int getGrade1() {
		return grade1;
	}
	public int getGrade2() {
		return grade2;
	}
	public int getGrade3() {
		return grade3;
	}
}
